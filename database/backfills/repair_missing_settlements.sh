#!/usr/bin/env bash
# Regenerates missing `settlements` rows from on-chain data.
#
# backfill_trades_tx_hash_check.sql can report trades without a matching
# settlement event. On-chain this cannot happen (both settle() and swap() emit
# the Settlement event after all Trade events of the same call), so such rows
# mean the settlements table has gaps. This script re-fetches the Settlement
# event logs of the affected blocks and prints INSERT statements (ON CONFLICT
# DO NOTHING) restoring the missing rows; auction_id/solution_uid remain NULL
# like for any other settlement not made by the current environment.
#
# Reads block numbers from stdin (one per line), writes SQL to stdout and
# progress/warnings to stderr. Blocks that have no Settlement logs on-chain
# are only warned about: either the RPC points at the wrong network or the
# trade rows of that block are corrupt — investigate manually, e.g. with
#   cargo run -p settlement-finder -- <block> --rpc-url <chain rpc>
#
# Usage:
#   1. List the affected blocks (association as in backfill_trades_tx_hash.sql):
#        psql <connection options> -tA -c "
#          SELECT DISTINCT t.block_number FROM trades t
#          WHERE NOT EXISTS (
#              SELECT 1 FROM settlements s
#              WHERE s.block_number = t.block_number
#              AND   s.log_index > t.log_index)
#          ORDER BY 1" > blocks.txt
#   2. RPC_URL=<chain rpc> ./repair_missing_settlements.sh < blocks.txt > repair.sql
#   3. Review repair.sql, apply it with psql -v ON_ERROR_STOP=1 -f repair.sql
#   4. Run backfill_trades_tx_hash.sql to resolve the trades, then re-run the
#      check.
#
# Requires foundry's `cast` and `jq`. One RPC call per block; for block lists
# beyond a few thousand entries consider fetching ranges instead.
set -euo pipefail

RPC_URL="${RPC_URL:?set RPC_URL to the RPC endpoint of the chain the database belongs to}"
# Deterministic deployment, same address on all supported chains.
SETTLEMENT_CONTRACT="${SETTLEMENT_CONTRACT:-0x9008D19f58AAbD9eD0D60971565AA8510560ab41}"

blocks=$(sort -nu | grep -v '^$' || true)
if [[ -z "$blocks" ]]; then
    echo "no blocks on stdin, nothing to do" >&2
    exit 0
fi

echo "-- generated by repair_missing_settlements.sh for $(wc -l <<<"$blocks") blocks"
echo "BEGIN;"
rows=0
warnings=0
while read -r block; do
    logs=$(cast logs --rpc-url "$RPC_URL" \
        --from-block "$block" --to-block "$block" \
        --address "$SETTLEMENT_CONTRACT" \
        'Settlement(address indexed solver)' --json)
    if [[ $(jq length <<<"$logs") -eq 0 ]]; then
        echo "WARNING: block $block has no Settlement logs on-chain" >&2
        warnings=$((warnings + 1))
        continue
    fi
    jq -r '
        def hexdec: ltrimstr("0x") | ascii_downcase | explode
            | reduce .[] as $c (0; . * 16 + (if $c >= 97 then $c - 87 else $c - 48 end));
        .[] | "INSERT INTO settlements (block_number, log_index, solver, tx_hash) VALUES ("
            + (.blockNumber | hexdec | tostring) + ", "
            + (.logIndex | hexdec | tostring) + ", "
            + "'\''\\x" + .topics[1][26:] + "'\'', "
            + "'\''\\x" + .transactionHash[2:] + "'\'') ON CONFLICT DO NOTHING;"
    ' <<<"$logs"
    rows=$((rows + $(jq length <<<"$logs")))
    echo "block $block: $(jq length <<<"$logs") settlement(s)" >&2
done <<<"$blocks"
echo "COMMIT;"
echo "done: $rows INSERTs generated, $warnings blocks without on-chain settlements" >&2

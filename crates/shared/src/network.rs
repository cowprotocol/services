/// The expected time between blocks on the network.
pub fn block_interval(network_id: &str, chain_id: u64) -> Option<Duration> {
    Some(Duration::from_secs(match (network_id, chain_id) {
        ("1", 1) => 12,
        ("5", 5) => 12,
        ("100", 100) => 5,
        _ => return None,
    }))
}

#[cfg(test)]
mod test {
    use {super::*, reqwest::Client};

    #[derive(Debug, serde::Deserialize)]
    #[serde(rename_all = "camelCase")]
    struct NetworkDetails {
        name: String,
        chain_id: u64,
        network_id: u64,
    }

    // cargo test -p shared -- network::test::matches_json_source_data --ignored
    #[tokio::test]
    #[ignore]
    async fn matches_json_source_data() {
        // This test is only expected to work immediately after updating the match
        // statement above. If ran later, then the chain list will probably have changed
        // and the test is going to fail when reaching the added network.

        let client = Client::default();

        let chain_list = client
            .get("https://chainid.network/chains.json")
            .send()
            .await
            .unwrap()
            .json::<Vec<NetworkDetails>>()
            .await
            .unwrap();

        for network in chain_list.iter() {
            assert_eq!(
                network_name(&network.network_id.to_string(), network.chain_id),
                network.name
            );
        }

        assert_eq!(network_name("any invalid network id", 1337), "<unknown>");

        // The length of the match block above is the same as the number of network
        assert_eq!(chain_list.len(), 1151);
    }
}

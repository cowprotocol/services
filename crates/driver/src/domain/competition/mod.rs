type BalanceGroup = (order::Trader, eth::TokenAddress, order::SellTokenBalance);
type Balances = HashMap<BalanceGroup, order::SellAmount>;

/// How many concurrent auction generations we size the settlement cache for.
/// Each auction can propose up to `max_solutions_to_propose` settlements;
/// keeping this many generations around lets a stale /settle for a previous
/// auction still find its settlement.
const MAX_CONCURRENT_AUCTIONS: usize = 5;

/// An ongoing competition. There is one competition going on per solver at any
/// time. The competition stores settlements to solutions generated by the
/// driver, and allows them to be executed onchain when requested later. The
/// solutions expire after a certain amount of time, at which point trying to
/// use them will return an `[Error::InvalidSolutionId]`.
/// Unified pool of submission slots. Contains one "direct" slot (solver EOA,
/// no forwarding overhead) and zero or more "delegated" slots (EIP-7702
/// submission accounts). The direct slot is preferred; delegated slots are
/// used only when the solver EOA is already busy with an in-flight settlement.
#[derive(Debug)]
struct SubmitterPool {
    /// 1-permit semaphore for the solver EOA (direct submission).
    direct_slot: Arc<tokio::sync::Semaphore>,
    /// EIP-7702 submission accounts. `None` in legacy single-EOA mode.
    delegated: Option<DelegatedSlots>,
    /// Limits total in-flight settle requests (including those waiting for a
    /// pool slot). This replaces the old settle-queue-based admission check
    /// and allows buffering requests beyond the number of physical slots.
    admission: Arc<tokio::sync::Semaphore>,
    solver_address: eth::Address,
}

#[derive(Debug)]
struct DelegatedSlots {
    release: mpsc::Sender<Account>,
    acquire: tokio::sync::Mutex<mpsc::Receiver<Account>>,
}

impl SubmitterPool {
    fn new(
        solver_address: eth::Address,
        submission_accounts: Vec<Account>,
        settle_queue_size: usize,
    ) -> Self {
        let num_delegated = submission_accounts.len();
        let delegated = if submission_accounts.is_empty() {
            None
        } else {
            let (tx, rx) = mpsc::channel(submission_accounts.len());
            for account in submission_accounts {
                tx.try_send(account)
                    .expect("channel has sufficient capacity");
            }
            Some(DelegatedSlots {
                release: tx,
                acquire: tokio::sync::Mutex::new(rx),
            })
        };
        let total_slots = 1 + num_delegated;
        let admission_capacity = total_slots + settle_queue_size;
        Self {
            direct_slot: Arc::new(tokio::sync::Semaphore::new(1)),
            delegated,
            admission: Arc::new(tokio::sync::Semaphore::new(admission_capacity)),
            solver_address,
        }
    }

    /// Acquire a submission slot. Prefers the direct solver EOA slot
    /// (cheaper, no forwarding). Falls back to a delegated EIP-7702 account
    /// when the direct slot is busy. Blocks if all slots are in use.
    async fn acquire(&self) -> Option<SubmitterGuard> {
        // Try the direct slot first (non-blocking).
        if let Ok(permit) = Arc::clone(&self.direct_slot).try_acquire_owned() {
            tracing::debug!("submitting directly from solver EOA");
            return Some(SubmitterGuard {
                inner: GuardInner::Direct(permit),
                solver_address: self.solver_address,
            });
        }

        // Direct slot busy — race it against delegated accounts. select_ok
        // ensures that if one future errors the other is still awaited.
        let fetch_direct_slot = async {
            let permit = Arc::clone(&self.direct_slot)
                .acquire_owned()
                .await
                .context("semaphore closed")?;
            tracing::debug!("submitting directly from solver EOA");
            Ok(GuardInner::Direct(permit))
        }
        .boxed();

        let fetch_eip7702_slot = async {
            let Some(ref delegated) = self.delegated else {
                return Err(anyhow::anyhow!("no EIP-7702 accounts configured"));
            };
            let mut channel = delegated.acquire.lock().await;
            let account = channel.recv().await.context("channel closed")?;
            tracing::debug!(submitter = ?account.address(), "using EIP-7702 submission account");
            Ok(GuardInner::Delegated {
                account,
                release: delegated.release.clone(),
            })
        }
        .boxed();

        let (inner, _remaining) =
            futures::future::select_ok([fetch_direct_slot, fetch_eip7702_slot])
                .await
                .inspect_err(|err| tracing::error!(?err, "could not acquire submission account"))
                .ok()?;

        Some(SubmitterGuard {
            inner,
            solver_address: self.solver_address,
        })
    }

    /// Try to reserve an admission permit without blocking. Returns `None` if
    /// the maximum number of in-flight settle requests has been reached.
    fn try_admit(&self) -> Option<tokio::sync::OwnedSemaphorePermit> {
        Arc::clone(&self.admission).try_acquire_owned().ok()
    }
}

/// RAII guard for a submission slot. Dropping returns the slot to the pool.
struct SubmitterGuard {
    inner: GuardInner,
    solver_address: eth::Address,
}

enum GuardInner {
    /// Solver EOA submits directly. Permit released on drop.
    Direct(#[expect(dead_code)] tokio::sync::OwnedSemaphorePermit),
    /// Delegated EIP-7702 submission. Account returned to channel on drop.
    Delegated {
        account: Account,
        release: mpsc::Sender<Account>,
    },
    /// Sentinel used during `Drop` to move data out of `self`.
    Dropped,
}

impl SubmitterGuard {
    fn submission_mode(&self) -> SubmissionMode {
        match &self.inner {
            GuardInner::Direct(_) => SubmissionMode::Direct(self.solver_address),
            GuardInner::Delegated { account, .. } => SubmissionMode::Delegated {
                submitter_eoa: account.address(),
                solver_eoa: self.solver_address,
            },
            GuardInner::Dropped => unreachable!(),
        }
    }
}

impl Drop for SubmitterGuard {
    fn drop(&mut self) {
        let inner = std::mem::replace(&mut self.inner, GuardInner::Dropped);
        if let GuardInner::Delegated { account, release } = inner {
            tokio::spawn(async move {
                if release.send(account).await.is_err() {
                    tracing::error!("failed to return submission account to pool: channel closed");
                }
            });
        }
        // Direct: OwnedSemaphorePermit drops here, releasing the permit.
    }
}

/// Wrapper around a spawned settlement task's [`JoinHandle`]. When dropped
/// (e.g. because the HTTP handler was cancelled by the autopilot), the task is
/// aborted after a short grace period to allow cleanup (e.g. cancelling a
/// pending mempool tx).
struct SettleTaskHandle(task::JoinHandle<Result<Settled, Error>>);

impl std::future::Future for SettleTaskHandle {
    type Output = Result<Settled, Error>;

    fn poll(
        mut self: std::pin::Pin<&mut Self>,
        cx: &mut std::task::Context<'_>,
    ) -> std::task::Poll<Self::Output> {
        std::pin::Pin::new(&mut self.0).poll(cx).map(|join_result| {
            join_result.map_err(|err| {
                tracing::error!(?err, "settle task panicked");
                Error::SubmissionError
            })?
        })
    }
}

impl Drop for SettleTaskHandle {
    fn drop(&mut self) {
        if !self.0.is_finished() {
            let handle = self.0.abort_handle();
            tokio::spawn(async move {
                tokio::time::sleep(std::time::Duration::from_secs(1)).await;
                handle.abort();
            });
        }
    }
}

#[derive(Debug)]
pub struct Competition {
    pub solver: Solver,
    pub eth: Ethereum,
    pub liquidity: infra::liquidity::Fetcher,
    pub liquidity_sources_notifier: infra::notify::liquidity_sources::Notifier,
    pub simulator: Simulator,
    pub mempools: Mempools,
    /// Cached solutions with the most recent solutions at the front.
    pub settlements: Mutex<VecDeque<Settlement>>,
    /// bad token and orders detector
    pub risk_detector: Arc<risk_detector::Detector>,
    fetcher: Arc<pre_processing::DataAggregator>,
    order_sorting_strategies: Vec<Arc<dyn sorting::SortingStrategy>>,
    submitter_pool: SubmitterPool,
}

impl Competition {
    #[expect(clippy::too_many_arguments)]
    pub fn new(
        solver: Solver,
        eth: Ethereum,
        liquidity: infra::liquidity::Fetcher,
        liquidity_sources_notifier: infra::notify::liquidity_sources::Notifier,
        simulator: Simulator,
        mempools: Mempools,
        risk_detector: Arc<risk_detector::Detector>,
        fetcher: Arc<DataAggregator>,
        order_sorting_strategies: Vec<Arc<dyn sorting::SortingStrategy>>,
    ) -> Arc<Self> {
        let submission_accounts = solver.submission_accounts().to_vec();
        if !submission_accounts.is_empty() {
            tracing::info!(
                count = submission_accounts.len(),
                "EIP-7702 parallel submission enabled"
            );
        }
        let settle_queue_size = solver.settle_queue_size();
        let submitter_pool =
            SubmitterPool::new(solver.address(), submission_accounts, settle_queue_size);

        Arc::new(Self {
            solver,
            eth,
            liquidity,
            liquidity_sources_notifier,
            simulator,
            mempools,
            settlements: Default::default(),
            risk_detector,
            fetcher,
            order_sorting_strategies,
            submitter_pool,
        })
    }

    /// Solve an auction as part of this competition.
    pub async fn solve(&self, request: Request<Body>) -> Result<Vec<Solved>, Error> {
        let start = Instant::now();
        let timer = ::observe::metrics::metrics()
            .on_auction_overhead_start("driver", "pre_processing_total");

        let tasks = self
            .fetcher
            .start_or_get_tasks_for_auction(request)
            .await
            .map_err(|err| {
                tracing::error!(?err, "pre-processing auction failed");
                Error::MalformedRequest
            })?;
        let mut auction = match tasks.auction.await {
            Ok(auction) => Arc::unwrap_or_clone(auction),
            // Infallible: Keep arm for same shared deadline result type as
            // the other preprocessing tasks
            Err(DeadlineExceeded) => {
                observe::auction_cancelled("auction", "already_expired");
                return Err(Error::DeadlineExceeded(DeadlineExceeded));
            }
        };

        let deadlines = auction.deadline(self.solver.timeouts());
        let auction_deadline = deadlines.auction();
        let driver_deadline = deadlines.driver();
        let effective_deadline = std::cmp::min(auction_deadline, driver_deadline);
        if effective_deadline <= chrono::Utc::now() {
            observe::auction_cancelled("auction", "deadline");
            return Ok(vec![]);
        }

        // Keep this guard alive for the whole solve path. Dropping it aborts the timer
        // so all clones and references of `deadline_cancel` depend on this value.
        let deadline_cancellation =
            deadline_cancellation::DeadlineCancellation::new(effective_deadline);
        let deadline_cancel = deadline_cancellation.token();

        let solver_address = self.solver.address();
        let order_sorting_strategies = self.order_sorting_strategies.clone();

        // Add the CoW AMM orders to the auction
        let cow_amm_orders = tokio::select! {
            result = tasks.cow_amm_orders => match result {
                Ok(cow_amm_orders) => cow_amm_orders,
                Err(DeadlineExceeded) => {
                    observe::auction_cancelled("cow_amm_orders", "deadline");
                    return Ok(vec![]);
                }
            },
            _ = deadline_cancel.cancelled() => {
                observe::auction_cancelled("cow_amm_orders", "deadline");
                return Ok(vec![]);
            }
        };
        auction.orders.extend(cow_amm_orders.iter().cloned());

        let sort_cancel = deadline_cancel.clone();
        let sort_orders_future = Self::run_blocking_with_timer("sort_orders", move || {
            // Use spawn_blocking() because a lot of CPU bound computations are happening
            // and we don't want to block the runtime for too long.
            if sort_cancel.is_cancelled() {
                return Err(DeadlineExceeded);
            }

            Self::sort_orders(
                auction,
                solver_address,
                order_sorting_strategies,
                &sort_cancel,
            )
        });

        // We can sort the orders and fetch auction data in parallel.
        let (auction_result, balances_result, app_data_result) = tokio::select! {
            result = async {
                tokio::join!(sort_orders_future, tasks.balances, tasks.app_data)
            } => result,
            _ = deadline_cancel.cancelled() => {
                observe::auction_cancelled("preprocessing", "deadline");
                return Ok(vec![]);
            }
        };

        let (auction, balances, app_data) = match (auction_result, balances_result, app_data_result)
        {
            (Ok(auction), Ok(balances), Ok(app_data)) => (auction, balances, app_data),
            (Err(DeadlineExceeded), _, _) => {
                observe::auction_cancelled("sort_orders", "deadline");
                return Ok(vec![]);
            }
            (_, Err(DeadlineExceeded), _) => {
                observe::auction_cancelled("balances", "deadline");
                return Ok(vec![]);
            }
            (_, _, Err(DeadlineExceeded)) => {
                observe::auction_cancelled("app_data", "deadline");
                return Ok(vec![]);
            }
        };

        let update_cancel = deadline_cancel.clone();
        let auction = tokio::select! {
            result = Self::run_blocking_with_timer("update_orders", move || {
                // Same as before with sort_orders, we use spawn_blocking() because a lot of CPU
                // bound computations are happening, and we want to avoid blocking
                // the runtime.
                if update_cancel.is_cancelled() {
                    return Err(DeadlineExceeded);
                }
                Self::update_orders(auction, balances, app_data, cow_amm_orders, &update_cancel)
            }) => result,
            _ = deadline_cancel.cancelled() => {
                observe::auction_cancelled("update_orders", "deadline");
                return Ok(vec![]);
            }
        };

        let auction = match auction {
            Ok(auction) => auction,
            Err(DeadlineExceeded) => {
                observe::auction_cancelled("update_orders", "deadline");
                return Ok(vec![]);
            }
        };

        // We can run bad token filtering and liquidity fetching in parallel
        let (liquidity, auction) = tokio::select! {
            result = async {
                tokio::join!(
                    async {
                        match self.solver.liquidity() {
                            solver::Liquidity::Fetch => tasks.liquidity.await,
                            solver::Liquidity::Skip => Ok(Arc::new(Vec::new())),
                        }
                    },
                    self.without_unsupported_orders(auction, &deadline_cancel)
                )
            } => {
                let (liquidity, auction) = result;

                let liquidity = match liquidity {
                    Ok(liquidity) => liquidity,
                    Err(DeadlineExceeded) => {
                        observe::auction_cancelled("liquidity", "deadline");
                        return Ok(vec![]);
                    }
                };

                let auction = match auction {
                    Ok(auction) => auction,
                    Err(DeadlineExceeded) => {
                        observe::auction_cancelled("unsupported_order_filtering", "deadline");
                        return Ok(vec![]);
                    }
                };

                (liquidity, auction)
            }
            _ = deadline_cancel.cancelled() => {
                observe::auction_cancelled("liquidity_filtering", "deadline");
                return Ok(vec![]);
            }
        };

        let elapsed = start.elapsed();
        metrics::get()
            .auction_preprocessing
            .with_label_values(&["total"])
            .observe(elapsed.as_secs_f64());
        drop(timer);
        tracing::debug!(?elapsed, "auction task execution time");

        if auction.orders.is_empty() {
            tracing::info!("no orders left after pre-processing; skipping solving");
            return Ok(vec![]);
        }

        let auction = &auction;

        // Fetch the solutions from the solver.
        let solutions = tokio::select! {
            result = self.solver.solve(auction, &liquidity) => {
                match result {
                    Ok(solutions) => solutions,
                    Err(err) if err.is_timeout() => {
                        notify::solver_timeout(&self.solver, auction.id());
                        observe::auction_cancelled("solver", "timeout");
                        return Ok(vec![]);
                    }
                    Err(err) => return Err(err.into()),
                }
            }
            _ = deadline_cancel.cancelled() => {
                observe::auction_cancelled("solver", "deadline");
                return Ok(vec![]);
            }
        };

        observe::postprocessing(&solutions, driver_deadline);

        // Discard solutions that don't have unique ID.
        let mut ids = HashSet::new();
        let solutions = solutions.into_iter().filter(|solution| {
            if !ids.insert(solution.id().clone()) {
                observe::duplicated_solution_id(self.solver.name(), solution.id());
                notify::duplicated_solution_id(&self.solver, auction.id(), solution.id());
                false
            } else {
                true
            }
        });

        // Discard empty solutions.
        let solutions = solutions.filter(|solution| {
            if solution.is_empty(auction.surplus_capturing_jit_order_owners()) {
                observe::empty_solution(self.solver.name(), solution.id());
                notify::empty_solution(&self.solver, auction.id(), solution.id().clone());
                false
            } else {
                true
            }
        });

        let all_solutions = match self.solver.solution_merging() {
            SolutionMerging::Allowed {
                max_orders_per_merged_solution,
            } => merge(solutions, auction, max_orders_per_merged_solution),
            SolutionMerging::Forbidden => solutions.collect(),
        };

        // Encode solutions into settlements (streamed).
        let encoded = all_solutions
            .into_iter()
            .map(|solution| async move {
                observe::encoding(solution.id());
                let settlement = solution
                    .clone()
                    .encode(
                        auction,
                        &self.eth,
                        &self.simulator,
                        self.solver.solver_native_token(),
                    )
                    .await;
                (solution, settlement)
            })
            .collect::<FuturesUnordered<_>>()
            .filter_map(|(solution, result)| async move {
                let id = solution.id().clone();
                let orders: Vec<_> = solution
                    .user_trades()
                    .map(|trade| trade.order().uid)
                    .collect();
                let has_haircut = solution.has_haircut();
                match result {
                    Ok(encoded) => {
                        self.risk_detector.encoding_succeeded(&orders);
                        Some(encoded)
                    }
                    // don't report on errors coming from solution merging
                    Err(_err) if id.solutions().len() > 1 => None,
                    Err(err) => {
                        self.risk_detector.encoding_failed(&orders);
                        observe::encoding_failed(
                            self.solver.name(),
                            &id,
                            &err,
                            has_haircut,
                            &orders,
                        );
                        // don't notify on errors for solutions with haircut
                        if !has_haircut {
                            notify::encoding_failed(&self.solver, auction.id(), &id, &err);
                        }
                        None
                    }
                }
            });

        // Encode settlements as they arrive until there are no more new settlements or
        // timeout is reached.
        let mut settlements = Vec::new();
        let mut encoded = std::pin::pin!(encoded);
        'encoding: loop {
            tokio::select! {
                settlement = encoded.next() => {
                    let Some(settlement) = settlement else {
                        break 'encoding;
                    };

                    settlements.push(settlement);
                }
                _ = deadline_cancel.cancelled() => {
                    observe::auction_cancelled("encoding", "deadline");
                    observe::postprocessing_timed_out(&settlements);
                    notify::postprocessing_timed_out(&self.solver, auction.id());
                    break 'encoding;
                }
            }
        }

        // Score the settlements.
        let scores = settlements
            .into_iter()
            .map(|settlement| {
                observe::scoring(&settlement);
                (
                    settlement.score(
                        &auction.native_prices(),
                        auction.surplus_capturing_jit_order_owners(),
                    ),
                    settlement,
                )
            })
            .collect_vec();

        // Filter out settlements which failed scoring.
        let scores = scores
            .into_iter()
            .filter_map(|(result, settlement)| {
                result
                    .inspect_err(|err| {
                        observe::scoring_failed(self.solver.name(), err);
                        notify::scoring_failed(
                            &self.solver,
                            auction.id(),
                            settlement.solution(),
                            err,
                        );
                    })
                    .ok()
                    .map(|score| (score, settlement))
            })
            .collect_vec();

        // Observe the scores.
        for (score, settlement) in scores.iter() {
            observe::score(settlement, score);
        }

        // Build scored settlements sorted best-first.
        let scored: Vec<(Solved, Settlement)> = scores
            .into_iter()
            .sorted_by_key(|(score, _)| Reverse(*score))
            .map(|(score, settlement)| {
                let solved = Solved {
                    id: settlement.solution().clone(),
                    score,
                    trades: settlement.orders(),
                    prices: settlement.prices(),
                    gas: Some(settlement.gas.estimate),
                };
                (solved, settlement)
            })
            .collect();

        if scored.is_empty() {
            return Ok(vec![]);
        }

        let max_to_propose = self.solver.max_solutions_to_propose();
        let mut scored: Vec<(Solved, Settlement)> =
            scored.into_iter().take(max_to_propose).collect();

        // Cache all settlements so they can be revealed/settled later. Keep
        // solutions from previous overlapping auctions around long enough
        // for their /settle to complete.
        {
            let mut lock = self.settlements.lock().unwrap();
            for (_, settlement) in &scored {
                lock.push_front(settlement.clone());
            }
            lock.truncate(max_to_propose * MAX_CONCURRENT_AUCTIONS);
        }

        if !deadline_cancel.is_cancelled() {
            let _ = self
                .resimulate_until_revert(&mut scored, auction, &deadline_cancel)
                .await;
        }

        Ok(scored.into_iter().map(|(solved, _)| solved).collect())
    }

    /// Re-simulate all proposed solutions on every new block and drop any
    /// that start reverting. Returns once every solution has reverted or
    /// the `cancel` token is triggered
    /// Mutates `scored` and the cached settlements in place.
    async fn resimulate_until_revert(
        &self,
        scored: &mut Vec<(Solved, Settlement)>,
        auction: &Auction,
        cancel: &CancellationToken,
    ) -> anyhow::Result<()> {
        let mut stream = ethrpc::block_stream::into_stream(self.eth.current_block().clone());
        loop {
            let Some(block) = tokio::select!(
                block = stream.next() => block,
                _ = cancel.cancelled() => {
                    observe::auction_cancelled("resimulation", "deadline");
                    return Ok(());
                }
            ) else {
                return Ok(());
            };

            let voided_ids: HashSet<u64> = tokio::select! {
                voided_ids = futures::future::join_all(scored.iter().map(|(solved, settlement)| {
                    self.reverts_on_block(block, solved, settlement, auction)
                })) => {
                    voided_ids.into_iter().flatten().collect()
                }
                _ = cancel.cancelled() => {
                    observe::auction_cancelled("resimulation", "deadline");
                    return Ok(());
                }
            };

            if voided_ids.is_empty() {
                continue;
            }

            scored.retain(|(solved, _)| !voided_ids.contains(&solved.id.get()));
            let mut lock = self
                .settlements
                .lock()
                .map_err(|_| anyhow::anyhow!("settlements mutex poisoned"))?;
            lock.retain(|s| !voided_ids.contains(&s.solution().get()));

            if scored.is_empty() {
                return Ok(());
            }
        }
    }

    /// Re-simulate a single solution and return its id if it started
    /// reverting on this block. Reports metrics and solver notifications as
    /// a side effect.
    async fn reverts_on_block(
        &self,
        block: BlockInfo,
        solved: &Solved,
        settlement: &Settlement,
        auction: &Auction,
    ) -> Option<u64> {
        let err = match self.simulate_settlement(settlement).await {
            Err(simulator::Error::Revert(err)) => err,
            _ => return None,
        };
        let has_haircut = settlement.has_haircut();
        observe::winner_voided(self.solver.name(), block, &err, has_haircut);
        if !has_haircut {
            notify::simulation_failed(
                &self.solver,
                auction.id(),
                settlement.solution(),
                &simulator::Error::Revert(err),
                true,
            );
        }
        Some(solved.id.get())
    }

    /// Orders already need to be sorted from most relevant to the least
    /// relevant so that we allocate balances for the most relevant first.
    /// Returns `DeadlineExceeded` if cancel has been triggered.
    fn sort_orders(
        mut auction: Auction,
        solver: eth::Address,
        order_sorting_strategies: Vec<Arc<dyn SortingStrategy>>,
        cancel: &CancellationToken,
    ) -> Result<Auction, DeadlineExceeded> {
        sorting::sort_orders(
            &mut auction.orders,
            &auction.tokens,
            &solver,
            &order_sorting_strategies,
            cancel,
        )?;
        Ok(auction)
    }

    /// Removes orders that cannot be filled due to missing funds of the owner
    /// and updates the fetched app data.
    /// It allocates available funds from left to right so the orders should
    /// already be sorted by priority going in.
    /// Returns `DeadlineExceeded` if cancel has been triggered.
    fn update_orders(
        mut auction: Auction,
        balances: Arc<Balances>,
        app_data: Arc<HashMap<order::app_data::AppDataHash, Arc<app_data::ValidatedAppData>>>,
        cow_amm_orders: Arc<Vec<Order>>,
        cancel: &CancellationToken,
    ) -> Result<Auction, DeadlineExceeded> {
        if cancel.is_cancelled() {
            return Err(DeadlineExceeded);
        }

        // Clone balances since we only aggregate data once but each solver needs
        // to use and modify the data individually.
        let mut balances = balances.as_ref().clone();
        let cow_amms: HashSet<_> = cow_amm_orders.iter().map(|o| o.uid).collect();

        let orders = std::mem::take(&mut auction.orders);
        let mut updated_orders = Vec::with_capacity(orders.len());

        // The auction that we receive from the `autopilot` assumes that there
        // is sufficient balance to completely cover all the orders. **This is
        // not the case** (as the protocol should not chose which limit orders
        // get filled for some given sell token balance). This loop goes through
        // the priority sorted orders and allocates the available user balance
        // to each order, and potentially scaling the order's `available` amount
        // down in case the available user balance is only enough to partially
        // cover the rest of the order.
        for mut order in orders {
            if cancel.is_cancelled() {
                return Err(DeadlineExceeded);
            }

            if cow_amms.contains(&order.uid) {
                // cow amm orders already get constructed fully initialized
                // so we don't have to handle them here anymore.
                // Without this short circuiting logic they would get filtered
                // out later because we don't bother fetching their balances
                // for performance reasons.
                updated_orders.push(order);
                continue;
            }

            // Update order app data if it was fetched.
            if let Some(fetched_app_data) = app_data.get(&order.app_data.hash()) {
                order.app_data = fetched_app_data.clone().into();
            }

            // Flashloan orders get their sell tokens from the flashloan at
            // settlement time, so skip the balance check.
            if order.app_data.flashloan().is_some() {
                updated_orders.push(order);
                continue;
            }

            // wrappers can produce the required funds at settlement time
            if !order.app_data.wrappers().is_empty() {
                updated_orders.push(order);
                continue;
            }

            let remaining_balance = match balances.get_mut(&(
                order.trader(),
                order.sell.token,
                order.sell_token_balance,
            )) {
                Some(balance) => balance,
                None => {
                    let reason = observe::OrderExcludedFromAuctionReason::CouldNotFetchBalance;
                    observe::order_excluded_from_auction(&order, reason);
                    continue;
                }
            };

            let max_sell = order::SellAmount(order.available().sell.amount.0);

            let allocated_balance = match order.partial {
                order::Partial::Yes { .. } => max_sell.min(*remaining_balance),
                order::Partial::No if max_sell <= *remaining_balance => max_sell,
                _ => order::SellAmount::default(),
            };
            if allocated_balance.0.is_zero() {
                observe::order_excluded_from_auction(
                    &order,
                    observe::OrderExcludedFromAuctionReason::InsufficientBalance,
                );
                continue;
            }
            // We need to scale the available amount in the order based on
            // allocated balance. We cannot naively just set the `available`
            // amount to equal the `allocated_balance` because of two reasons:
            // 1. They are in different units. `available` is a `TargetAmount` which means
            //    it would be in buy token for buy orders and not in sell token like the
            //    `allocated_balance`
            // 2. Account for fees. Even in the case of sell orders, `available` is
            //    potentially different to `allocated_balance` because of fee scaling. For
            //    example, imagine a partially fillable order selling 100 tokens with a fee
            //    of 10 for a user with a balance of 50. The `allocated_balance` would be 50
            //    tokens, but the `available` amount needs to be less! We want the
            //    following: `available + (fee * available / sell) <= allocated_balance`
            if let order::Partial::Yes { available } = &mut order.partial {
                *available = order::TargetAmount(
                    math::mul_ratio(available.0, allocated_balance.0, max_sell.0)
                        .unwrap_or_default(),
                );
            }

            if order.available().is_zero() {
                observe::order_excluded_from_auction(
                    &order,
                    observe::OrderExcludedFromAuctionReason::OrderWithZeroAmountRemaining,
                );
                continue;
            }

            remaining_balance.0 -= allocated_balance.0;
            updated_orders.push(order);
        }
        auction.orders = updated_orders;
        Ok(auction)
    }

    /// Runs a blocking function on a background thread, timing it using the
    /// given stage name. This helper does not cancel or abort the blocking
    /// closure itself. Any deadline handling must be implemented by the
    /// closure using a `CancellationToken`. If the async caller returns
    /// early, it may still continue running until it reaches
    /// its own cancellation check or completes.
    pub async fn run_blocking_with_timer<T, F>(stage: &'static str, f: F) -> T
    where
        F: FnOnce() -> T + Send + 'static,
        T: Send + 'static,
    {
        task::spawn_blocking(move || {
            let _timer = metrics::get().processing_stage_timer(stage);
            let _timer2 = ::observe::metrics::metrics().on_auction_overhead_start("driver", stage);
            f()
        })
        .await
        .expect(
            "Either runtime was shut down before spawning the task or no OS threads are \
             available; no sense in handling those errors",
        )
    }

    pub async fn reveal(
        &self,
        solution_id: u64,
        auction_id: auction::Id,
    ) -> Result<Revealed, Error> {
        let settlement = self
            .settlements
            .lock()
            .unwrap()
            .iter()
            .find(|s| s.solution().get() == solution_id && s.auction_id == auction_id)
            .cloned()
            .ok_or(Error::SolutionNotAvailable)?;
        Ok(Revealed {
            internalized_calldata: settlement
                .transaction(settlement::Internalization::Enable)
                .input
                .clone(),
            uninternalized_calldata: settlement
                .transaction(settlement::Internalization::Disable)
                .input
                .clone(),
        })
    }

    /// Execute the solution generated as part of this competition. Use
    /// [`Competition::solve`] to generate the solution.
    pub async fn settle(
        self: &Arc<Self>,
        auction_id: auction::Id,
        solution_id: u64,
        submission_deadline: BlockNo,
    ) -> Result<Settled, Error> {
        let admission_permit = self.submitter_pool.try_admit().ok_or_else(|| {
            tracing::warn!("too many pending settlements; settle request rejected");
            Error::TooManyPendingSettlements
        })?;

        if self.eth.current_block().borrow().number >= submission_deadline.0 {
            return Err(DeadlineExceeded.into());
        }

        let this = Arc::clone(self);
        let tracing_span = tracing::Span::current();
        // Spawn as a separate task to enable concurrent EIP-7702 submissions.
        // SettleTaskHandle aborts the task (with a grace period) if the caller
        // disconnects.
        let handle = tokio::spawn(
            async move {
                let result = this
                    .process_settle_request(auction_id, solution_id, submission_deadline)
                    .await;
                observe::settled(this.solver.name(), &result);
                drop(admission_permit);
                result
            }
            .instrument(tracing_span),
        );
        SettleTaskHandle(handle).await
    }

    pub fn ensure_settle_queue_capacity(&self) -> Result<(), Error> {
        if self.submitter_pool.admission.available_permits() == 0 {
            tracing::warn!("no idle submission slots; auction is rejected");
            Err(Error::TooManyPendingSettlements)
        } else {
            Ok(())
        }
    }

    async fn process_settle_request(
        &self,
        auction_id: auction::Id,
        solution_id: u64,
        submission_deadline: BlockNo,
    ) -> Result<Settled, Error> {
        let settlement = {
            let mut lock = self.settlements.lock().unwrap();
            let index = lock
                .iter()
                .position(|s| s.solution().get() == solution_id && s.auction_id == auction_id)
                .ok_or(Error::SolutionNotAvailable)?;
            // remove settlement to ensure we can't settle it twice by accident
            lock.swap_remove_front(index)
                .ok_or(Error::SolutionNotAvailable)?
        };

        // Asynchronously notify liquidity sources to not block settlement execution.
        {
            let liquidity_sources_notifier_clone = self.liquidity_sources_notifier.clone();
            let settlement_clone = settlement.clone();
            tokio::spawn(async move {
                match liquidity_sources_notifier_clone
                    .settlement(&settlement_clone)
                    .await
                {
                    Ok(_) => {}
                    Err(err) => {
                        tracing::error!(?err, "Failed to notify liquidity sources on settlement");
                    }
                }
            });
        }

        notify::settlement_started(&self.solver, settlement.auction_id, settlement.solution());

        // Acquire a submission slot. The pool prefers the direct solver EOA
        // (no forwarding overhead); falls back to a delegated EIP-7702
        // submission account when the solver EOA is busy.
        let guard = self
            .submitter_pool
            .acquire()
            .await
            .ok_or(Error::SubmissionError)?;
        let mode = guard.submission_mode();

        let executed = self
            .mempools
            .execute(&settlement, submission_deadline, &mode)
            .await;

        notify::executed(
            &self.solver,
            settlement.auction_id,
            settlement.solution(),
            &executed,
        );

        match executed {
            Err(_) => Err(Error::SubmissionError),
            Ok(tx_hash) => Ok(Settled {
                internalized_calldata: settlement
                    .transaction(settlement::Internalization::Enable)
                    .input
                    .clone(),
                uninternalized_calldata: settlement
                    .transaction(settlement::Internalization::Disable)
                    .input
                    .clone(),
                tx_hash,
            }),
        }
    }

    /// The ID of the auction being competed on.
    pub fn auction_id(&self, solution_id: u64) -> Option<auction::Id> {
        self.settlements
            .lock()
            .unwrap()
            .iter()
            .find(|s| s.solution().get() == solution_id)
            .map(|s| s.auction_id)
    }

    /// Returns whether the settlement can be executed or would revert.
    async fn simulate_settlement(&self, settlement: &Settlement) -> Result<(), simulator::Error> {
        let tx = settlement.transaction(settlement::Internalization::Enable);
        let gas_needed_for_tx = self.simulator.gas(tx).await?;
        if gas_needed_for_tx > settlement.gas.limit {
            return Err(simulator::Error::Revert(RevertError {
                err: SimulatorError::GasExceeded(gas_needed_for_tx, settlement.gas.limit),
                tx: tx.clone(),
                block: self.eth.current_block().borrow().number.into(),
            }));
        }
        Ok(())
    }

    #[instrument(skip_all)]
    async fn without_unsupported_orders(
        &self,
        mut auction: Auction,
        cancel: &CancellationToken,
    ) -> Result<Auction, DeadlineExceeded> {
        if cancel.is_cancelled() {
            return Err(DeadlineExceeded);
        }

        if !self.solver.config().flashloans_enabled {
            auction.orders.retain(|o| o.app_data.flashloan().is_none());
        }

        if cancel.is_cancelled() {
            return Err(DeadlineExceeded);
        }

        self.risk_detector
            .filter_unsupported_orders_in_auction(auction, cancel)
            .await
    }
}

const MAX_SOLUTIONS_TO_MERGE: usize = 10;

/// Creates a vector with all possible combinations of the given solutions.
/// The result is sorted descending by score.
fn merge(
    solutions: impl Iterator<Item = Solution>,
    auction: &Auction,
    max_orders_per_merged_solution: usize,
) -> Vec<Solution> {
    let mut merged: Vec<Solution> = Vec::new();
    // Limit the number of solutions to merge to avoid combinatorial explosion
    // (2^MAX_SOLUTIONS).
    for solution in solutions.take(MAX_SOLUTIONS_TO_MERGE) {
        let mut extension = vec![];
        for already_merged in merged.iter() {
            if let Ok(merged) = solution.merge(already_merged, max_orders_per_merged_solution) {
                observe::merged(&solution, already_merged, &merged);
                extension.push(merged);
            }
        }

        // At least insert the current solution
        extension.push(solution);
        merged.extend(extension);
    }

    // Sort merged solutions descending by score.
    merged.sort_by_key(|solution| {
        Reverse(
            solution
                .scoring(
                    &auction.native_prices(),
                    auction.surplus_capturing_jit_order_owners(),
                )
                .map(|score| score.0)
                .unwrap_or_default(),
        )
    });
    merged
}

/// Solution information sent to the protocol by the driver before the solution
/// ranking happens.
#[derive(Debug)]
pub struct Solved {
    pub id: solution::Id,
    pub score: eth::Ether,
    pub trades: HashMap<order::Uid, Amounts>,
    pub prices: HashMap<eth::TokenAddress, eth::TokenAmount>,
    pub gas: Option<eth::Gas>,
}

#[derive(Debug)]
pub struct Amounts {
    pub side: order::Side,
    /// The sell token and limit sell amount of sell token.
    pub sell: eth::Asset,
    /// The buy token and limit buy amount of buy token.
    pub buy: eth::Asset,
    /// The effective amount that left the user's wallet including all fees.
    pub executed_sell: eth::TokenAmount,
    /// The effective amount the user received after all fees.
    pub executed_buy: eth::TokenAmount,
}

#[derive(Clone, Debug)]
pub struct PriceLimits {
    pub sell: eth::TokenAmount,
    pub buy: eth::TokenAmount,
}

/// Winning solution information revealed to the protocol by the driver before
/// the onchain settlement happens. Calldata is first time revealed at this
/// point.
#[derive(Debug)]
pub struct Revealed {
    /// The internalized calldata is the final calldata that appears onchain.
    pub internalized_calldata: Bytes,
    /// The uninternalized calldata must be known so that the CoW solver team
    /// can manually enforce certain rules which can not be enforced
    /// automatically.
    pub uninternalized_calldata: Bytes,
}

#[derive(Debug)]
pub struct Settled {
    /// The transaction hash in which the solution was submitted.
    pub tx_hash: eth::TxId,
    pub internalized_calldata: Bytes,
    /// The uninternalized calldata must be known so that the CoW solver team
    /// can manually enforce certain rules which can not be enforced
    /// automatically.
    pub uninternalized_calldata: Bytes,
}

#[derive(Debug, thiserror::Error)]
pub enum Error {
    #[error(
        "no solution is available yet, this might mean that /settle was called before /solve \
         returned"
    )]
    SolutionNotAvailable,
    #[error("{0:?}")]
    DeadlineExceeded(#[from] DeadlineExceeded),
    #[error("solver error: {0:?}")]
    Solver(#[from] solver::Error),
    #[error("failed to submit the solution")]
    SubmissionError,
    #[error("too many pending settlements for the same solver")]
    TooManyPendingSettlements,
    #[error("no valid orders found in the auction")]
    NoValidOrdersFound,
    #[error("could not parse the request")]
    MalformedRequest,
}

